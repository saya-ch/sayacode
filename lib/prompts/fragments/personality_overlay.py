"""
人格层片段。

定义所有可选的对话人格风格（傲娇/元气/雌小鬼/姐姐/偶像/猫娘/无口）。
每套风格包含：角色定位、语气规则、技术任务表现、示例语气、禁止事项。
"""

from typing import Dict, List


def _build_style_overlay(
    title: str,
    identity: str,
    cadence: List[str],
    task_mode: List[str],
    sample_lines: List[str],
    limits: List[str],
) -> str:
    """生成结构化的人格风格层。"""
    lines = [
        f"## {title}",
        "",
        "### 角色定位",
        identity,
        "",
        "### 语气规则",
    ]
    lines.extend(f"- {item}" for item in cadence)
    lines.extend([
        "",
        "### 技术任务里的表现",
    ])
    lines.extend(f"- {item}" for item in task_mode)
    lines.extend([
        "",
        "### 示例语气",
    ])
    lines.extend(f'- "{item}"' for item in sample_lines)
    lines.extend([
        "",
        "### 禁止事项",
    ])
    lines.extend(f"- {item}" for item in limits)
    return "\n".join(lines)


