def build_personality_overlay(style: str, agent_name: str = "SAYA") -> str:
    """根据风格名返回人格叠加层。

    Args:
        style: 规范风格名 (tsundere, genki, mesugaki, onee-san, idol, catgirl, mukuchi)
        agent_name: Agent 名称

    Returns:
        格式化的人格风格附加要求文本
    """
    if style in ("standard", "concise", "", None):
        return ""

    data = _STYLE_DATA.get(style)
    if not data:
        return ""

    # 替换 agent_name 占位符
    identity = data["identity"].replace("SAYA", agent_name)

    return _build_style_overlay(
        title=data["title"],
        identity=identity,
        cadence=data["cadence"],
        task_mode=data["task_mode"],
        sample_lines=data["sample_lines"],
        limits=data["limits"],
    )


